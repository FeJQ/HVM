	{
		return NULL;
	}

	while (replaceCodeSize < hookInstructionLength)
	{
		if ((*(unsigned char*)(src)+replaceCodeSize & 0xF0) == 0x70 ||
			*(unsigned char*)src + replaceCodeSize == 0xE8 ||
			*(unsigned char*)src + replaceCodeSize == 0xE9
			)
		{
			// hook
			return NULL;
		}
		replaceCodeSize += LDE((PVOID)((ULONG_PTR)src + replaceCodeSize), 64);
	}
	if (replaceCodeSize > sizeof(pHookData->shellCode))
	{
		return NULL;//;
	}

	//DetourShellCode
	ULONG_PTR desAddressValue = (ULONG_PTR)des;
	ULARGE_INTEGER* desAddress = (ULARGE_INTEGER*)&desAddressValue;
	JmoCode jmpCodeToDes = { 0 };
	if (UtilIsDebug())
	{
		jmpCodeToDes.breakPoint = 0x90;
	}
	else
	{
		jmpCodeToDes.breakPoint = 0x90;
	}
	jmpCodeToDes.push = 0x68;
	jmpCodeToDes.addressLow = desAddress->LowPart;
	jmpCodeToDes.mov = 0x042444C7;
	jmpCodeToDes.addressHigh = desAddress->HighPart;
	jmpCodeToDes.ret = 0xC3;

	//OriginalShellCode
	ULONG_PTR srcAddressValue = (ULONG_PTR)src + replaceCodeSize;
	ULARGE_INTEGER* srcAddress = (ULARGE_INTEGER*)&srcAddressValue;
	JmoCode jmpCodeToSrc = { 0 };
	if (UtilIsDebug())
	{
		jmpCodeToSrc.breakPoint = 0x90;
	}
	else
	{
		jmpCodeToSrc.breakPoint = 0x90;
	}
	jmpCodeToSrc.push = 0x68;
	jmpCodeToSrc.addressLow = srcAddress->LowPart;
	jmpCodeToSrc.mov = 0x042444C7;
	jmpCodeToSrc.addressHigh = srcAddress->HighPart;
	jmpCodeToSrc.ret = 0xC3;

	KSPIN_LOCK spinLock;
	KIRQL irql;

	memset(pHookData, 0x90, sizeof(HookData));
	pHookData->orgFuncAddr = src;
	pHookData->copyLength = replaceCodeSize;
	pHookData->proxyFuncAddr = (ULONG_PTR)des;
	memcpy(pHookData->shellCode, src, replaceCodeSize);
	memcpy(pHookData->backToFunc, &jmpCodeToSrc, sizeof(JmoCode));
	mem_protect_close();	
	KeInitializeSpinLock(&spinLock);
	KeAcquireSpinLock(&spinLock, &irql);
	memset(src, 0x90, replaceCodeSize);
	memcpy(src, &jmpCodeToDes, sizeof(JmoCode));
